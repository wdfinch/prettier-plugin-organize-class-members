export class Car {
  getYear() {
    return this.year
  }

  getName() {
    return this.name
  }

  serialize() {
    return this.name
  }

  deserialize() {
    return this.name
  }

  render() {
    return this.name
  }

  setName() {
    return this.name
  }

  constructor(name, year) {
    this.name = name
    this.year = year
  }
}
